package twitter;

public class BoundingBoxTwitter
{
	
	private float bottom_left_longitude;
	private float bottom_left_latitude;
	private float upper_right_longitude;
	private float upper_right_latitude;
	
	public float getBottom_left_longitude() {
		return bottom_left_longitude;
	}
	public void setBottom_left_longitude(float bottom_left_longitude) {
		this.bottom_left_longitude = bottom_left_longitude;
	}
	public float getBottom_left_latitude() {
		return bottom_left_latitude;
	}
	public void setBottom_left_latitude(float bottom_left_latitude) {
		this.bottom_left_latitude = bottom_left_latitude;
	}
	public float getUpper_right_longitude() {
		return upper_right_longitude;
	}
	public void setUpper_right_longitude(float upper_right_longitude) {
		this.upper_right_longitude = upper_right_longitude;
	}
	public float getUpper_right_latitude() {
		return upper_right_latitude;
	}
	public void setUpper_right_latitude(float upper_right_latitude) {
		this.upper_right_latitude = upper_right_latitude;
	}

	

}